/*
Решение задачи: могло ли в первой группе быть больше людей, чем во второй, при условии, что на вход подается количество задач, решенных первой группой, второй группой и общее количество задач.
*/

#include <iostream>

int main()
{
	unsigned int n = 0, a = 0, b = 0;
	std::cin >> a >> b >> n;
	if (a > ((b + n - 1) / n)) {
		std::cout << "Yes";
	}
	else {
		std::cout << "No";
	}
	return 0;
}
